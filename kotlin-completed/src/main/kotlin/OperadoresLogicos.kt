    /*
    * "!=" varifica se é diferente de algum valor
    * "==" varifica se é igual ao algum valor
    * "is" (instanceof) verifica se um tipo é igual a outro tipo
    */

    /*
    * Operadores Lógicos => true ou false
    */

    val x = 2
    val y = 4
    val z = "xjz"

